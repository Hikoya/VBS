/**
 * This function is only called when the email server of the recipient does not
 * support HTML emails
 *
 * @param param0 URL of the callback link and the hostname of the application
 * @returns A string
 */
function text({ newURL, host }) {
  return `Sign in to ${host}\n${newURL}\n\n`;
}

/**
 * Options for the Next-Auth library, including sending of emails for passwordless-authentication,
 * as well as checking of session and populating session with custom details
 */
export const options = {
  providers: [
    EmailProvider({
      server: {
        host: process.env.EMAIL_SERVER_HOST,
        port: Number(process.env.EMAIL_SERVER_PORT),
        auth: {
          user: process.env.EMAIL_SERVER_USER,
          pass: process.env.EMAIL_SERVER_PASSWORD,
        },
      },
      from: process.env.EMAIL_FROM,
      async sendVerificationRequest({
        identifier: email,
        url,
        provider: { server, from },
      }) {
        const filterString = `${process.env.NEXTAUTH_URL}/api/auth/callback/`;
        const urlSplit = url.replace(filterString, '');

        const newURL = `${process.env.NEXTAUTH_URL}/sys/callback/${urlSplit}`;

        const { host } = new URL(newURL);
        const transport = nodemailer.createTransport(server);
        await transport.sendMail({
          to: email,
          from,
          subject: 'KEVII VBS: Sign in',
          text: text({ newURL, host }),
          html: html({ newURL, email }),
        });
      },
    }),
  ],
  adapter: PrismaAdapter(prisma),
  secret: process.env.NEXTAUTH_SECRET,
  session: {
    jwt: true,
    maxAge: 1 * 24 * 60 * 60, // 1 day
  },
  jwt: {
    secret: process.env.NEXTAUTH_JWT_SIGNING_PRIVATE_KEY,
    encryption: true,
  },
  pages: {
    signIn: '/sys/signin',
    verifyRequest: '/sys/verify-request',
    error: '/sys/error',
  },
  callbacks: {
    async signIn({ user, email }) {
      let isAllowedToSignIn = true;
      try {
        if (
          Object.prototype.hasOwnProperty.call(email, 'verificationRequest')
        ) {
          isAllowedToSignIn = false;

          const email: string = (user.email as string).trim().toLowerCase();

          const doesUserExist = await prisma.users.findUnique({
            where: {
              email: email,
            },
          });

          if (doesUserExist !== null) {
            isAllowedToSignIn = true;
          }
        }

        if (isAllowedToSignIn) {
          return true;
        }
      } catch (error) {
        console.log(error);
        await logger('nextAuthOptions Signin', 'user', error.message);
      }

      return '/sys/unauthorized';
    },
    async session({ session, user }) {
      try {
        if (!session.user.username) {
          const userFromDB: User = await prisma.users.findUnique({
            where: {
              email: user.email,
            },
          });

          if (userFromDB != null) {
            const newSession: Session = JSON.parse(JSON.stringify(session));
            newSession.user.email = userFromDB.email;
            newSession.user.username = userFromDB.name;
            newSession.user.admin = userFromDB.admin;

            if (userFromDB.acceptedTerm !== undefined) {
              newSession.user.acceptedTerm = userFromDB.acceptedTerm;
            }

            return newSession;
          }
        } else {
          return session;
        }
      } catch (error) {
        console.log(error);
        await logger('nextAuthOptions Session', 'user', error.message);
      }

      return session;
    },
  },
};
